#!/usr/bin/env node
/**
 * Patch GUI boot — register memory overlay executors in browser bundle path.
 * Writes dist/overlay/* + dist/assets/gui-overlay-boot.mjs, patches index.html.
 */
import {
  readFileSync, writeFileSync, readdirSync, mkdirSync, cpSync,
} from 'node:fs';
import { join, dirname, basename } from 'node:path';
import { fileURLToPath } from 'node:url';

const ROOT = join(dirname(fileURLToPath(import.meta.url)), '..');
const distDir = process.argv[2] ? join(ROOT, process.argv[2]) : join(ROOT, 'dist');
const assetsDir = join(distDir, 'assets');
const overlayDir = join(distDir, 'overlay');
const indexHtml = join(distDir, 'index.html');

const BOOT_MARKER = 'gui-overlay-boot.mjs';
const OVERLAY_FILES = [
  ['gui-overlay-browser.mjs', 'gui-overlay.mjs'],
  'session/session-hub.mjs',
  'session/apply-step-core.mjs',
  'memory-graph/register-gui.mjs',
];

function findBundle() {
  return readdirSync(assetsDir)
    .filter((f) => f.startsWith('index-') && f.endsWith('.js'))
    .map((f) => join(assetsDir, f));
}

function syncOverlayFiles() {
  mkdirSync(join(overlayDir, 'session'), { recursive: true });
  mkdirSync(join(overlayDir, 'memory-graph'), { recursive: true });
  for (const entry of OVERLAY_FILES) {
    const [rel, destRel] = Array.isArray(entry) ? entry : [entry, entry];
    const src = join(ROOT, 'lib', rel);
    const dest = join(overlayDir, destRel);
    mkdirSync(dirname(dest), { recursive: true });
    cpSync(src, dest);
  }
}

function writeBootEntry(bundleName) {
  const out = join(assetsDir, 'gui-overlay-boot.mjs');
  const content = `/**
 * PolarUI GUI executor overlay — memory browser executors.
 * AUTO-GENERATED by scripts/patch-gui-overlay-boot.mjs
 */
import { r as registerExecutor } from './${bundleName}';
import { registerGuiOverlays } from '../overlay/gui-overlay.mjs';

await registerGuiOverlays(registerExecutor);
console.log('[PolarUI] GUI overlay: ScenarioMemory registered');
`;
  writeFileSync(out, content, 'utf8');
  console.log(`wrote gui-overlay-boot: ${out} -> ./${bundleName}`);
}

function patchIndexHtml() {
  let html = readFileSync(indexHtml, 'utf8');
  const tag = `<script type="module" src="/assets/${BOOT_MARKER}"></script>`;
  if (html.includes(BOOT_MARKER)) {
    console.log('skip index.html (already patched)');
    return;
  }
  const anchor = '<script type="module" crossorigin src="/assets/';
  const pos = html.indexOf(anchor);
  if (pos < 0) {
    console.error('index.html anchor not found');
    process.exit(1);
  }
  const lineEnd = html.indexOf('</script>', pos) + '</script>'.length;
  html = `${html.slice(0, lineEnd)}\n  ${tag}${html.slice(lineEnd)}`;
  writeFileSync(indexHtml, html, 'utf8');
  console.log(`patched index.html: +${BOOT_MARKER}`);
}

const bundles = findBundle();
if (!bundles.length) {
  console.error('no index-*.js bundle in dist/assets');
  process.exit(1);
}

syncOverlayFiles();
writeBootEntry(basename(bundles[0]));
patchIndexHtml();
console.log('patch-gui-overlay-boot: done');
