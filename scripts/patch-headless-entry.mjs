#!/usr/bin/env node
/**
 * Patch bundle boot (mbn): skip Vue createApp/mount when globalThis.__POLAR_HEADLESS__.
 * GUI 路径不受影响；Node headless 在 import 前设 __POLAR_HEADLESS__=true。
 * 同时生成 dist/assets/headless.mjs 薄入口。
 */
import { readFileSync, writeFileSync, readdirSync } from 'node:fs';
import { join, dirname, basename } from 'node:path';
import { fileURLToPath } from 'node:url';

const ROOT = join(dirname(fileURLToPath(import.meta.url)), '..');
const assetsDir = join(ROOT, 'dist/assets');

const OLD = 'const p=fon(wbn);p.use(gon()),p.mount("#app")}mbn();export{';
const NEW = 'if(globalThis.__POLAR_HEADLESS__)return;const p=fon(wbn);p.use(gon()),p.mount("#app")}mbn();export{';
const MARKER = 'if(globalThis.__POLAR_HEADLESS__)return;const p=fon(wbn)';

function findBundle() {
  return readdirSync(assetsDir)
    .filter((f) => f.startsWith('index-') && f.endsWith('.js'))
    .map((f) => join(assetsDir, f));
}

function writeHeadlessEntry(bundleFile) {
  const name = basename(bundleFile);
  const out = join(assetsDir, 'headless.mjs');
  const content = `/**
 * PolarUI headless engine entry — 无 Vue mount。
 * AUTO-GENERATED by scripts/patch-headless-entry.mjs
 */
globalThis.__POLAR_HEADLESS__ = true;

export {
  e as executeGraph,
  l as parseWorkflow,
  r as registerExecutor,
} from './${name}';
`;
  writeFileSync(out, content, 'utf8');
  console.log(`wrote headless entry: ${out} -> ./${name}`);
}

let patched = 0;
let bundleName = null;
for (const path of findBundle()) {
  const src = readFileSync(path, 'utf8');
  bundleName = basename(path);
  if (src.includes(MARKER)) {
    console.log(`skip headless-entry (already patched): ${path}`);
    continue;
  }
  if (!src.includes(OLD)) {
    console.error(`headless-entry needle not found in ${path}`);
    process.exit(1);
  }
  writeFileSync(path, src.replace(OLD, NEW), 'utf8');
  console.log(`patched headless skip-mount: ${path}`);
  patched += 1;
}

if (!bundleName) {
  console.error('no index-*.js bundle found in dist/assets');
  process.exit(1);
}
writeHeadlessEntry(bundleName);

console.log(patched ? `patch-headless-entry: ${patched} file(s)` : 'patch-headless-entry: nothing to do (headless.mjs refreshed)');

