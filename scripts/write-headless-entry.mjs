#!/usr/bin/env node
/**
 * Write dist-next/assets/headless.mjs thin re-export after vite build.
 * Main bundle already skips Vue mount when globalThis.__POLAR_HEADLESS__ (src/main.ts).
 * Also writes workflow-loader.mjs shim for Node dynamic import('./workflow-loader').
 */
import { readFileSync, writeFileSync, readdirSync, existsSync } from 'node:fs';
import { join, dirname, basename } from 'node:path';
import { fileURLToPath } from 'node:url';

const ROOT = join(dirname(fileURLToPath(import.meta.url)), '..');
const outRoot = process.argv[2] ?? 'dist-next';
const assetsDir = join(ROOT, outRoot, 'assets');

function findBundle() {
  if (!existsSync(assetsDir)) return null;
  const hit = readdirSync(assetsDir)
    .filter((f) => f.startsWith('index-') && f.endsWith('.js'))
    .sort()
    .at(-1);
  return hit ? join(assetsDir, hit) : null;
}

/** @param {string} exportClause */
function hasExportAlias(exportClause, letter) {
  return exportClause.split(',').some((part) => {
    const m = part.trim().match(/^[$\w]+\s+as\s+(\w+)$/);
    return m?.[1] === letter;
  });
}

const bundleFile = findBundle();
if (!bundleFile) {
  console.error(`write-headless-entry: no index-*.js in ${assetsDir}`);
  process.exit(1);
}

const bundleName = basename(bundleFile);
const src = readFileSync(bundleFile, 'utf8');
if (!src.includes('__POLAR_HEADLESS__')) {
  console.warn('write-headless-entry: bundle missing __POLAR_HEADLESS__ guard — headless may mount Vue');
}

const exportMatch = src.match(/export\{([^}]+)\};?\s*$/);
if (!exportMatch) {
  console.error('write-headless-entry: bundle export block not found');
  process.exit(1);
}

const clause = exportMatch[1];
for (const letter of ['e', 'l', 'r']) {
  if (!hasExportAlias(clause, letter)) {
    console.error(`write-headless-entry: bundle missing export alias "${letter}" in: export{${clause}}`);
    process.exit(1);
  }
}

const out = join(assetsDir, 'headless.mjs');
const content = `/**
 * PolarUI headless engine entry — 无 Vue mount。
 * AUTO-GENERATED by scripts/write-headless-entry.mjs
 */
globalThis.__POLAR_HEADLESS__ = true;

export {
  e as executeGraph,
  l as parseWorkflow,
  r as registerExecutor,
} from './${bundleName}';
`;
writeFileSync(out, content, 'utf8');
console.log(`wrote headless entry: ${out} -> ./${bundleName}`);

const workflowLoader = `/**
 * Node headless shim for dynamic import('./workflow-loader') in the main bundle.
 * AUTO-GENERATED by scripts/write-headless-entry.mjs
 */
import { readFileSync, existsSync } from 'node:fs';
import { join, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const moduleDir = dirname(fileURLToPath(import.meta.url));
const searchRoots = [
  join(moduleDir, '../../workflows'),
  join(moduleDir, '../../../workflows'),
];

async function parseWorkflowText(raw) {
  const { parseWorkflow } = await import('./headless.mjs');
  return parseWorkflow(raw);
}

export async function loadWorkflowByRef(ref) {
  const file = ref.endsWith('.json') ? ref : \`\${ref}.json\`;
  for (const wfDir of searchRoots) {
    const path = join(wfDir, file);
    if (existsSync(path)) return parseWorkflowText(readFileSync(path, 'utf8'));
  }
  return null;
}

export async function loadWorkflowJsonText(ref) {
  const g = await loadWorkflowByRef(ref);
  return g ? JSON.stringify(g.toApiFormat(), null, 2) : null;
}
`;
writeFileSync(join(assetsDir, 'workflow-loader.js'), workflowLoader, 'utf8');
writeFileSync(join(assetsDir, 'workflow-loader.mjs'), workflowLoader, 'utf8');
console.log(`wrote workflow-loader shim: ${join(assetsDir, 'workflow-loader.js')}`);
